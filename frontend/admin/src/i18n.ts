const DEFAULT_LOCALE: AdminLocale =
  typeof navigator !== "undefined" && navigator.language.toLowerCase().startsWith("zh")
    ? "zh-CN"
    : "en-US";

let currentLocale: AdminLocale = DEFAULT_LOCALE;

function interpolate(
  template: string,
  params: TranslationParams = {},
): string {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key];
    return value == null ? "" : String(value);
  });
}

export function getCurrentLocale(): AdminLocale {
  return currentLocale;
}

export function isZhLocale(locale = currentLocale): boolean {
  return locale === "zh-CN";
}

export function initializeI18n(): void {
  currentLocale = DEFAULT_LOCALE;
  if (typeof document !== "undefined") {
    document.documentElement.lang = currentLocale;
  }
}

export function t(
  text: string,
  params?: TranslationParams,
): string {
  const template =
    currentLocale === "zh-CN" ? ZH_CN_MESSAGES[text] ?? text : text;
  return interpolate(template, params);
}
